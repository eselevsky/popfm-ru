const capitalize = (s: string) => {
  if (typeof s !== 'string' || !s) return s;
  return s.charAt(0).toUpperCase() + s.slice(1);
};
export function translateGenre(genre: string): string {
  if (!genre) return '';
  const lowerGenre = genre.toLowerCase();
  return genreTranslations[lowerGenre] ? capitalize(genreTranslations[lowerGenre]) : capitalize(genre);
}
export function translateCountry(country: string): string {
  if (!country) return '';
  return countryTranslations[country] || country;
}